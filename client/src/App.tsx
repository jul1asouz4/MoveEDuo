import { Toaster } from "@/components/ui/sonner";
import { TooltipProvider } from "@/components/ui/tooltip";
import NotFound from "@/pages/NotFound";
import { Route, Switch } from "wouter";
import ErrorBoundary from "./components/ErrorBoundary";
import { ThemeProvider } from "./contexts/ThemeContext";
import Home from "./pages/Home";


function Router() {
  return (
    <Switch>
      <Route path={"/"} component={Home} />
      <Route path={"/404"} component={NotFound} />
      {/* Final fallback route */}
      <Route component={NotFound} />
    </Switch>
  );
}

// NOTE: Design Philosophy - Modernismo Dinâmico (MoveED)
// Color Palette: Deep Blue (#0F3A7D), Emerald Green (#10B981), Warm Orange (#FF8C42)
// Typography: Poppins for headings, Inter for body
// Layout: Asymmetric with alternating content and images
// Animations: Smooth fade-in, hover elevation effects, scroll animations
// This design conveys professionalism, opportunity, and global connection

function App() {
  return (
    <ErrorBoundary>
      <ThemeProvider
        defaultTheme="light"
        // switchable
      >
        <TooltipProvider>
          <Toaster />
          <Router />
        </TooltipProvider>
      </ThemeProvider>
    </ErrorBoundary>
  );
}

export default App;
